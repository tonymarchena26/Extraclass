    private void btnSign_inActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSign_inActionPerformed

    try {
        int nLines = 0;
        int i=0;
        String [] users = null;
        String line;
        
        sc = new Scanner (new File("D:\\users.txt"));
        File f = new File ("D:/users.txt");
        FileReader fr = new FileReader (f);
        BufferedReader br = new BufferedReader (fr);
        
        try {
            while((line=br.readLine())!=null){
                nLines++;
             } } catch (IOException ex) {
            Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        users = new String[nLines];//tama√±o del arreglo
        
        while(sc.hasNextLine()){
            users[i++] = sc.nextLine(); //almaceno cada linea en posicion del arreglo
        }
        
        tries++;
        user = txtUser.getText();
        pwd = txtPwd.getText();
        
        Security s = new Security();
        s.validUser(users, user, pwd, tries);
        
       
        
        
    } catch (FileNotFoundException ex) {
        Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
    }
                
        
    }//GEN-LAST:event_btnSign_inActionPerformed

